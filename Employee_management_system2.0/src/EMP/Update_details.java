    private void populateIdComboBox() {
    try {
        DBConnection db =new DBConnection();
        String query = "SELECT id FROM empdata";
        
        db.resultSet = db.stm.executeQuery(query);
        while (db.resultSet.next()) {
            String id = db.resultSet.getString("id");
            ID.addItem(id);
        }
        db.resultSet.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
}
