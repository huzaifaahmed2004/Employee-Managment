    private void Update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_btnActionPerformed
    DBConnection db =new DBConnection();
    String selectedId = ID.getSelectedItem().toString();
        String username = Name.getText();
        String fname = FName.getText();
        String age = Age.getText();
        String dob = DOB.getText();
        String address = Add.getText();
        String phone = Phone.getText();
        String email = Email.getText();
        String education = Edu.getText();
        String jobPost = JP.getText();
        String cnic = CNIC.getText();

        try {
              String q = "UPDATE empdata SET username = ?, Fname = ?, age = ?, DOB = ?, address = ?, phone = ?, email = ?, Education = ?, JobPost = ?, CNIC = ? WHERE ID = ?";
            Connection conect=db.conn;
        PreparedStatement statement = conect.prepareStatement(q) ;
             statement.setString(1, username);
            statement.setString(2, fname);
            statement.setString(3, age);
            statement.setString(4, dob);
            statement.setString(5, address);
            statement.setString(6, phone);
            statement.setString(7, email);
            statement.setString(8, education);
            statement.setString(9, jobPost);
            statement.setString(10, cnic);
            statement.setString(11, selectedId);

           int rowsUpdated = statement.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null,"Employee information updated successfully");
                this.setVisible(false);
            } else {
                
            JOptionPane.showMessageDialog(null,"Failed to update employee information.");
            }
            statement.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    
    }//GEN-LAST:event_Update_btnActionPerformed

